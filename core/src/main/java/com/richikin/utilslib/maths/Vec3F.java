package com.richikin.utilslib.maths;

public class Vec3F
{
    public float x;
    public float y;
    public float z;

    public Vec3F()
    {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }

    public Vec3F(int x, int y, int z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
